#include <stdio.h>

int main() {
       int day=5;
       switch(day)
       {
           case 1:
           printf("monday");
           break;
           case 2:
           printf("tuesday");
           break;
           case 3:
           printf("wednesday");
           break;
           default:
           printf("no other days");
       }
        return 0;
}